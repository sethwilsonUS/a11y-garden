// Axe-core violation structure
interface AxeNode {
  html: string;
  target: string[];
  failureSummary?: string;
}

interface AxeViolation {
  id: string;
  impact?: "critical" | "serious" | "moderate" | "minor";
  description: string;
  help: string;
  helpUrl: string;
  nodes: AxeNode[];
}

interface Audit {
  url: string;
  domain: string;
  pageTitle?: string;
  letterGrade: string;
  score: number;
  scannedAt: number;
  aiSummary?: string;
  topIssues?: string[];
  violations: {
    critical: number;
    serious: number;
    moderate: number;
    minor: number;
    total: number;
  };
  scanMode?: "full" | "safe";
  truncated?: boolean;
  rawViolations?: string;
}

function generateMarkdownReport(audit: Audit, reportUrl?: string): string {
  const date = new Date(audit.scannedAt).toLocaleDateString("en-US", {
    weekday: "long",
    year: "numeric",
    month: "long",
    day: "numeric",
  });

  let markdown = `# ${audit.pageTitle ? `${audit.pageTitle} — Accessibility Report` : "Accessibility Report"}

**URL:** ${audit.url}
**Grade:** ${audit.letterGrade} (${audit.score}/100)
**Scanned:** ${date}${audit.scanMode === "safe" ? "\n**Note:** This scan ran in Safe Mode due to site complexity. Not all checks were performed." : ""}${reportUrl ? `\n**Full Report:** ${reportUrl}` : ""}

> *This report reflects automated checks only and is not a substitute for a comprehensive WCAG audit.*

---

## Violations Summary

| Severity | Count |
|----------|-------|
| Critical | ${audit.violations.critical} |
| Serious | ${audit.violations.serious} |
| Moderate | ${audit.violations.moderate} |
| Minor | ${audit.violations.minor} |
| **Total** | **${audit.violations.total}** |
`;

  if (audit.aiSummary) {
    markdown += `
---

## AI Summary

${audit.aiSummary}
`;
  }

  if (audit.topIssues && audit.topIssues.length > 0) {
    markdown += `
---

## Top Issues to Address

${audit.topIssues.map((issue, i) => `${i + 1}. ${issue}`).join("\n")}
`;
  }

  if (audit.rawViolations) {
    try {
      const violations: AxeViolation[] = JSON.parse(audit.rawViolations);
      if (violations.length > 0) {
        markdown += `
---

## Violations by Rule

`;
        const severityOrder = ["critical", "serious", "moderate", "minor"];
        const grouped = severityOrder.reduce((acc, severity) => {
          acc[severity] = violations.filter((v) => v.impact === severity);
          return acc;
        }, {} as Record<string, AxeViolation[]>);

        for (const severity of severityOrder) {
          const items = grouped[severity];
          if (items && items.length > 0) {
            markdown += `### ${severity.charAt(0).toUpperCase() + severity.slice(1)}\n\n`;
            for (const violation of items) {
              markdown += `- **${violation.help}** (\`${violation.id}\`) — ${violation.nodes.length} element${violation.nodes.length === 1 ? "" : "s"}\n`;
            }
            markdown += "\n";
          }
        }
      }
    } catch {
      // Ignore JSON parse errors
    }
  }

  markdown += `
---

*Report generated by A11y Garden*
`;

  return markdown;
}

// Severity config using CSS vars
const severityConfig = {
  critical: { cssVar: "severity-critical", label: "Critical" },
  serious: { cssVar: "severity-serious", label: "Serious" },
  moderate: { cssVar: "severity-moderate", label: "Moderate" },
  minor: { cssVar: "severity-minor", label: "Minor" },
};

function CopyReportButton({ audit }: { audit: Audit }) {
  const [copied, setCopied] = useState(false);

  const handleCopy = () => {
    const reportUrl = typeof window !== "undefined" ? window.location.href : undefined;
    const markdown = generateMarkdownReport(audit, reportUrl);
    navigator.clipboard.writeText(markdown);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <button
      onClick={handleCopy}
      className="btn-secondary cursor-pointer"
    >
      {copied ? (
        <>
          <svg className="w-5 h-5 text-accent" fill="none" stroke="currentColor" viewBox="0 0 24 24" aria-hidden="true">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
          </svg>
          Copied!
        </>
      ) : (
        <>
          <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24" aria-hidden="true">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12h6m-6 4h6m2 5H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
          </svg>
          Copy Report
        </>
      )}
    </button>
  );
}

function DetailedViolationsAccordion({ rawViolations }: { rawViolations: string }) {
  const [isOpen, setIsOpen] = useState(false);
  const [expandedViolations, setExpandedViolations] = useState<Set<string>>(new Set());

  let violations: AxeViolation[] = [];
  try {
    violations = JSON.parse(rawViolations);
  } catch {
    return null;
  }

  if (violations.length === 0) return null;

  const severityOrder: Array<"critical" | "serious" | "moderate" | "minor"> = ["critical", "serious", "moderate", "minor"];
  const grouped = severityOrder.reduce((acc, severity) => {
    acc[severity] = violations.filter((v) => v.impact === severity);
    return acc;
  }, {} as Record<string, AxeViolation[]>);

  const toggleViolation = (id: string) => {
    setExpandedViolations((prev) => {
      const next = new Set(prev);
      if (next.has(id)) {
        next.delete(id);
      } else {
        next.add(id);
      }
      return next;
    });
  };

  return (
    <section className="garden-bed overflow-hidden">
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="w-full px-6 py-4 flex items-center justify-between bg-theme-secondary hover:bg-theme-tertiary transition-colors cursor-pointer rounded-t-2xl"
        aria-expanded={isOpen}
        aria-controls="detailed-violations-panel"
      >
        <div className="flex items-center gap-3">
          <svg className="w-5 h-5 text-theme-secondary" fill="none" stroke="currentColor" viewBox="0 0 24 24" aria-hidden="true">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5H7a2 2 0 00-2 2v12a2 2 0 002 2h10a2 2 0 002-2V7a2 2 0 00-2-2h-2M9 5a2 2 0 002 2h2a2 2 0 002-2M9 5a2 2 0 012-2h2a2 2 0 012 2m-6 9l2 2 4-4" />
          </svg>
          <span className="font-display font-semibold text-theme-primary">
            Detailed Violations
          </span>
          <span className="text-sm text-theme-muted">
            ({violations.length} {violations.length === 1 ? "rule" : "rules"} violated)
          </span>
        </div>
        <svg
          className={`w-5 h-5 text-theme-secondary transition-transform duration-200 ${isOpen ? "rotate-180" : ""}`}
          fill="none"
          stroke="currentColor"
          viewBox="0 0 24 24"
          aria-hidden="true"
        >
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
        </svg>
      </button>

      {isOpen && (
        <div id="detailed-violations-panel" className="p-6 space-y-6 bg-theme-primary border-t border-theme">
          {severityOrder.map((severity) => {
            const items = grouped[severity];
            if (!items || items.length === 0) return null;
            const config = severityConfig[severity];

            return (
              <div key={severity}>
                <h3
                  className="text-sm font-semibold uppercase tracking-wide mb-3"
                  style={{ color: `var(--${config.cssVar})` }}
                >
                  {config.label} ({items.length})
                </h3>
                <div className="space-y-3">
                  {items.map((violation) => {
                    const isExpanded = expandedViolations.has(violation.id);
                    return (
                      <div
                        key={violation.id}
                        className="rounded-lg border overflow-hidden"
                        style={{
                          backgroundColor: `var(--${config.cssVar}-bg)`,
                          borderColor: `var(--${config.cssVar}-border)`,
                        }}
                      >
                        <button
                          onClick={() => toggleViolation(violation.id)}
                          className="w-full px-4 py-3 flex items-start justify-between text-left cursor-pointer hover:opacity-80 transition-opacity"
                          aria-expanded={isExpanded}
                          aria-controls={`violation-${violation.id}`}
                        >
                          <div className="flex-1 min-w-0">
                            <p className="font-medium text-theme-primary">
                              {violation.help}
                            </p>
                            <p className="text-sm text-theme-muted mt-1">
                              <code className="text-xs bg-theme-tertiary px-1.5 py-0.5 rounded font-mono">
                                {violation.id}
                              </code>
                              <span className="mx-2" aria-hidden="true">·</span>
                              {violation.nodes.length} {violation.nodes.length === 1 ? "element" : "elements"} affected
                            </p>
                          </div>
                          <svg
                            className={`w-5 h-5 text-theme-muted flex-shrink-0 ml-2 transition-transform duration-200 ${isExpanded ? "rotate-180" : ""}`}
                            fill="none"
                            stroke="currentColor"
                            viewBox="0 0 24 24"
                            aria-hidden="true"
                          >
                            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                          </svg>
                        </button>

                        {isExpanded && (
                          <div id={`violation-${violation.id}`} className="px-4 pb-4 space-y-4 border-t" style={{ borderColor: `var(--${config.cssVar}-border)` }}>
                            <p className="text-sm text-theme-secondary pt-3">
                              {violation.description}
                            </p>
                            <a
                              href={violation.helpUrl}
                              target="_blank"
                              rel="noopener noreferrer"
                              className="inline-flex items-center gap-1 text-sm text-accent hover:underline transition-colors"
                            >
                              Learn how to fix this
                              <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24" aria-hidden="true">
                                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14" />
                              </svg>
                            </a>

                            <div>
                              <p className="text-xs font-semibold text-theme-muted uppercase tracking-wide mb-2">
                                Affected Elements ({violation.nodes.length})
                              </p>
                              <div className="space-y-2 max-h-64 overflow-y-auto">
                                {violation.nodes.slice(0, 10).map((node, idx) => {
                                  const getFixInstructions = (summary: string | undefined) => {
                                    if (!summary) return null;
                                    const lines = summary.split("\n").map(l => l.trim()).filter(Boolean);
                                    const instructions = lines.filter(
                                      l => !l.match(/^Fix (all|any) of the following:?$/i)
                                    );
                                    return instructions.length > 0 ? instructions : null;
                                  };

                                  const fixInstructions = getFixInstructions(node.failureSummary);

                                  return (
                                    <div key={idx} className="bg-theme-tertiary rounded-lg p-3">
                                      <pre className="text-xs text-theme-secondary overflow-x-auto whitespace-pre-wrap break-all font-mono">
                                        {node.html}
                                      </pre>
                                      {fixInstructions && (
                                        <div className="mt-2 pt-2 border-t border-theme">
                                          <p className="text-xs font-medium text-theme-muted mb-1">How to fix:</p>
                                          <ul className="text-xs text-theme-secondary space-y-1">
                                            {fixInstructions.map((instruction, i) => (
                                              <li key={i} className="flex items-start gap-2">
                                                <span className="text-accent mt-0.5" aria-hidden="true">·</span>
                                                <span>{instruction}</span>
                                              </li>
                                            ))}
                                          </ul>
                                        </div>
                                      )}
                                    </div>
                                  );
                                })}
                                {violation.nodes.length > 10 && (
                                  <p className="text-xs text-theme-muted text-center py-2">
                                    ...and {violation.nodes.length - 10} more elements
                                  </p>
                                )}
                              </div>
                            </div>
                          </div>
                        )}
                      </div>
                    );
                  })}
                </div>
              </div>
            );
          })}
        </div>
      )}
    </section>
  );
}

function AuditHistoryTimeline({
  url,
  currentAuditId,
}: {
  url: string;
  currentAuditId: Id<"audits">;
}) {
  const history = useQuery(api.audits.getAuditHistory, {
    url,
    excludeAuditId: currentAuditId,
  });

  if (!history || history.length === 0) return null;

  // Grade color using the same CSS vars the app already uses
  const gradeColor = (grade: string) => {
    if (grade === "A" || grade === "B") return "var(--accent)";
    if (grade === "C") return "var(--severity-moderate)";
    if (grade === "D") return "var(--severity-serious)";
    return "var(--severity-critical)";
  };

  return (
    <section>
      <h2 className="text-xl font-display font-bold text-theme-primary mb-4">
        Audit History
      </h2>
      <div className="garden-bed p-5">
        <div className="space-y-0">
          {history.map((pastAudit, index) => {
            const { score: displayScore, grade: displayGrade } =
              pastAudit.gradingVersion === GRADING_VERSION
                ? { score: pastAudit.score, grade: pastAudit.letterGrade }
                : calculateGrade(pastAudit.violations);

            return (
              <div key={pastAudit._id} className="flex items-center gap-4">
                {/* Timeline connector */}
                <div className="flex flex-col items-center">
                  <div
                    className="w-8 h-8 rounded-full flex items-center justify-center font-display font-bold text-sm border-2 flex-shrink-0"
                    style={{
                      color: gradeColor(displayGrade),
                      borderColor: gradeColor(displayGrade),
                      backgroundColor: "var(--bg-primary)",
                    }}
                  >
                    {displayGrade}
                  </div>
                  {index < history.length - 1 && (
                    <div className="w-px h-6 bg-theme-tertiary" />
                  )}
                </div>

                {/* Audit info */}
                <Link
                  href={`/results/${pastAudit._id}`}
                  className="flex-1 flex items-center justify-between py-2 group"
                >
                  <div>
                    <span className="text-sm text-theme-secondary group-hover:text-accent transition-colors">
                      {new Date(pastAudit.scannedAt).toLocaleDateString("en-US", {
                        month: "short",
                        day: "numeric",
                        year: "numeric",
                      })}
                    </span>
                    <span className="text-xs text-theme-muted ml-2">
                      Score: {displayScore}/100
                    </span>
                    {pastAudit.scanMode === "safe" && (
                      <span className="text-xs text-theme-muted ml-2">(partial scan)</span>
                    )}
                  </div>
                  <svg className="w-4 h-4 text-theme-muted group-hover:text-accent transition-colors" fill="none" stroke="currentColor" viewBox="0 0 24 24" aria-hidden="true">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5l7 7-7 7" />
                  </svg>
                </Link>
              </div>
            );
          })}
        </div>
      </div>
    </section>
  );
}

